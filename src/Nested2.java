public class Nested2
{
    public static void main(String[] args)
    {
        for (int num = 4; num < 5; num ++)
        {
            System.out.println("*****");
            System.out.println("****");
            System.out.println("***");
            System.out.println("**");
            System.out.println("*");
        }
    }
}
